 <h2 class ="text-center">Artifical Intelligence and NLG</h2>

    <p class = "text-center"> Natural language processing is a promising sub-field of Artificial Intelligence (AI),
        the end goal of which is to create text that can be read as if it was written by a human.
        Early NLG has been attempted for decades,
        but we are only really approaching human-level performance in the past few years.
        Some algorithms and methods have beaten human performance in specific metrics,
        and it is probable that this is just the beginning.
        NLG may be one of the last fields in which human beings can still outperform Artificial Neural Networks,
        but it appears that our dominance in this area will come to an end soon enough.
        Recently Recurrent Neural Networks and Convolutional Neural Networks (CNNs) have begun to prove dominant in the field of NLG,
        and their rapid recent improvements show no sign of slowing. However,
        Artificial Intelligencesâ€™ (AIs) performance in the realm of NLG is primarily restricted to more straightforward,
        shorter tasks, like object recognition and caption generation. In more substantial,
        more complex tasks like poetry generation,
        AIs still perform significantly worse than the best human beings and progress here is slow.
        Significant advantages that AIs have over human beings is their ability to produce significant quantities of work very quickly,
        and it is plausible that in the future people will use AIs to enhance their work by getting an AI to generate many ideas that the person will then look through and choose one to use.</p>

